let number_day = prompt('Numero del dia')

switch (number_day) {
    case '1':
        alert('Es Lunes!')
        break
    case '2':
        alert('Es Martes!')
        break
    case '3':
        alert('Es Miercoles!')
        break
    case '4':
        alert('Es Jueves!')
        break
    case '5':
        alert('Es Viernes')
        break
    case '6':
        alert('Es Sabado!')
        break
    case '7':
        alert('Es Domingo!')
        break

    default : alert('NO ES NUMERO VALIDO')
}
